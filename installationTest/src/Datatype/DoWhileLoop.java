package Datatype;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		
		int i=1;
		
		do {
			
			System.out.println(i);
			i++;
			
		}
		while (i<=10);
		
		System.out.println("********in do while it will execute the verable first then go for condation *********");
		System.out.println("*****for example see below************");
		
		
		int j=1;
		
		do {
			System.out.println(j);

			j++;
			
			
		}while (j<=10);
		

		
		
		
		
		
		
	}

}
