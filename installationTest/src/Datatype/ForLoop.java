package Datatype;

public class ForLoop {

	public static void main(String[] args) {

		
		System.out.println("******************For loop Incrementl*************************************************");
		for (int i=1;i<=10;i++) {
			
			System.out.println(i);
		}
		System.out.println("******************For loop Decremental***************************************************");
		for ( int j=10;j>0;j--)
		{
			System.out.println(j);
		}
		
		
		
		
	}

}
